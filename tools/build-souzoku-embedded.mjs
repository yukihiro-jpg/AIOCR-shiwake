/*
  相続管理アプリ（単一HTML）を、総合アプリ(AI-OCR)へ iframe 埋め込みするための
  embedded.ts を生成するスクリプト。

  使い方:
    node tools/build-souzoku-embedded.mjs path/to/souzoku/index.html
    （第1引数を省略すると、既定パス ../souzoku-kanri/index.html を探します）

  生成物: src/modules/souzoku/embedded.ts  （export const SOUZOKU_HTML）

  ポイント:
   - <head> 直後に <script>window.__SOUZOKU_EMBED__=true;</script> を注入し、
     相続アプリ側の同期モジュールを「ホストcore経由（postMessageブリッジ）」モードで動かす。
   - HTML 全体は JSON.stringify で安全にTS文字列リテラル化する。
*/
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const arg = process.argv[2];
const SRC = arg ? resolve(process.cwd(), arg) : resolve(__dirname, '../../souzoku-kanri/index.html');
const OUT = resolve(__dirname, '../src/modules/souzoku/embedded.ts');

let html = readFileSync(SRC, 'utf8');

const FLAG = '<script>window.__SOUZOKU_EMBED__=true;</script>';
if (!html.includes('window.__SOUZOKU_EMBED__=true')) {
  html = html.replace(/<head>/i, (m) => m + '\n' + FLAG);
} // 既にフラグ注入済みなら二重注入しない

const banner =
  '/* AUTO-GENERATED by tools/build-souzoku-embedded.mjs — DO NOT EDIT.\n' +
  '   相続管理アプリ(index.html)をそのまま埋め込んだHTML文字列です。\n' +
  '   __SOUZOKU_EMBED__ フラグにより、同期はホストcore経由(postMessage)で行われます。 */\n';

const ts = banner + 'export const SOUZOKU_HTML: string = ' + JSON.stringify(html) + ';\n';

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, ts, 'utf8');
console.log('[build-souzoku-embedded] wrote', OUT, '(' + html.length + ' chars HTML)');
